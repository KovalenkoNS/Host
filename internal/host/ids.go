// Пакет host — управляющий слой SCG: подключение, запуск и надзор за
// приложениями. Хост НЕ содержит предметной логики: он исключительно
// запускает приложения и показывает их результаты.
//
// Приложение = ПРОЕКТ (папка с исполняемыми файлами), подключаемый из одного
// из двух источников:
//   - ЛОКАЛЬНЫЙ: путь к папке на машине пользователя (используется на месте);
//   - GITHUB: публичный репозиторий, скачиваемый целиком в external-apps.
//
// В проекте может быть несколько .exe; пользователь выбирает нужный и
// запускает его как есть (raw): хост создаёт отдельный процесс ОС, отдаёт
// код выхода и stdout. Приложения изолированы: каждый запуск — свой процесс.
package host

import (
	"crypto/rand"
	"encoding/hex"
)

// NewCallID — идентификатор задания/вызова: 16 случайных байт в hex.
// Хост намеренно не зависит от internal/domain: управляющее приложение —
// верхний слой без предметной логики.
func NewCallID() string {
	var b [16]byte
	if _, err := rand.Read(b[:]); err != nil {
		panic("host: crypto/rand недоступен: " + err.Error())
	}
	return hex.EncodeToString(b[:])
}
